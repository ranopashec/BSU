#include <stdio.h>
#include <math.h>

// Функция для вычисления второго катета и площади треугольника
int main() {
    double a, c, b, area;

    // Ввод данных: катет и гипотенуза
    printf("Введите катет и гипотенузу: ");
    scanf("%lf %lf", &a, &c);


  //новый комментарий для задания про GitHub Desktop
  //.
  //.
  //$

    // Проверяем, что гипотенуза больше катета
    if (c <= a) {
        printf("Ошибка: гипотенуза должна быть больше катета!\n");
        return 1;
    }

    // Вычисляем второй катет по теореме Пифагора
    b = sqrt(c * c - a * a);

    // Вычисляем площадь треугольника
    area = 0.5 * a * b;

    // Вывод результата
    printf("Второй катет: %.2f\n", b);
    printf("Площадь треугольника: %.2f\n", area);

    return 0;
}
